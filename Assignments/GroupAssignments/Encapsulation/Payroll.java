package Encapsulation;

public class Payroll{

	private String rank;
	private double salary;
	
	public String getRank() {
		return rank;
	}
	public void setRank(String rank) {
		this.rank=rank;
	}
	public double getSalary() {
		return salary;
	}
	public void setSalary(double salary) {
		this.salary=salary;
	}	
}